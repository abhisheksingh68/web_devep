<?php
// WAP in PHP to show inner labeled block of switch.
$n=readline("Enter a number b/w 1 to 5 :");
switch($n){
case 1:
{
echo "case 1 is executing...\n";
}

case 2:
{
echo "case 2 is executing...\n";
}

case 3:
{
echo "case 3 is executing...\n";
}

default:
{
echo "default case is executing...\n";
}

}

?>